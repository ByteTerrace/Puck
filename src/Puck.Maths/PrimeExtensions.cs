    /// <summary>Builds <see cref="StrongPseudoprimeFilter"/> by fingerprinting every entry of <see cref="StrongPseudoprimesBase2"/>.</summary>
    /// <returns>The occupancy bitmap consulted by <see cref="IsPrime(uint)"/> before it falls back to a binary search.</returns>
    private static ulong[] CreateStrongPseudoprimeFilter() {
        var filter = new ulong[1024];
        var table = StrongPseudoprimesBase2;

        for (var i = 0; (i < table.Length); ++i) {
            var fingerprint = ((table[i] * 0x9E3779B1U) >> 16);

            filter[(fingerprint >> 6)] |= (1UL << ((int)(fingerprint & 63U)));
        }

        return filter;
    }
    /// <summary>Computes the multiplicative inverse of <paramref name="divisor"/> modulo 2³² by Newton–Hensel lifting.</summary>
    /// <param name="divisor">The odd divisor.</param>
    /// <returns>The value <c>d′</c> satisfying <c>divisor · d′ ≡ 1 (mod 2³²)</c>.</returns>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static uint GetModularInverse(uint divisor) {
        var inverse = divisor;

        inverse *= (2U - (divisor * inverse));
        inverse *= (2U - (divisor * inverse));
        inverse *= (2U - (divisor * inverse));
        inverse *= (2U - (divisor * inverse));

        return inverse;
    }
    /// <summary>Computes the negated multiplicative inverse of <paramref name="modulus"/> modulo 2³².</summary>
    /// <param name="modulus">The odd modulus.</param>
    /// <returns>The value <c>m′</c> satisfying <c>modulus · m′ ≡ −1 (mod 2³²)</c>, consumed by <see cref="MontgomeryMultiply(uint, uint, uint, uint)"/>.</returns>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static uint GetMontgomeryInverse(uint modulus) =>
        (0U - GetModularInverse(divisor: modulus));
    /// <summary>Doubles a Montgomery-form residue modulo <paramref name="modulus"/> without branching.</summary>
    /// <param name="modulus">The odd modulus.</param>
    /// <param name="residue">The residue to double; must be less than <paramref name="modulus"/>.</param>
    /// <returns>The doubled residue, less than <paramref name="modulus"/>.</returns>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static uint MontgomeryDouble(uint modulus, uint residue) {
        var adjusted = ((((ulong)residue) << 1) - modulus);

        return ((uint)(adjusted + (((ulong)modulus) & ((ulong)(((long)adjusted) >> 63)))));
    }
    /// <summary>Multiplies two Montgomery-form residues modulo <paramref name="modulus"/> using a single REDC reduction.</summary>
    /// <param name="left">The first residue; must be less than <paramref name="modulus"/>.</param>
    /// <param name="modulus">The odd modulus.</param>
    /// <param name="modulusInverse">The negated inverse of <paramref name="modulus"/> obtained from <see cref="GetMontgomeryInverse(uint)"/>.</param>
    /// <param name="right">The second residue; must be less than <paramref name="modulus"/>.</param>
    /// <returns>The Montgomery-form product, less than <paramref name="modulus"/>.</returns>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static uint MontgomeryMultiply(uint left, uint modulus, uint modulusInverse, uint right) {
        var product = (((ulong)left) * right);
        var factor = (((uint)product) * modulusInverse);
        var carry = ((((ulong)((uint)product)) + 0xFFFFFFFFUL) >> 32);
        var reduced = (((product >> 32) + ((((ulong)factor) * modulus) >> 32)) + carry);
        var adjusted = (reduced - modulus);

        return ((uint)(adjusted + (((ulong)modulus) & ((ulong)(((long)adjusted) >> 63)))));
    }
    /// <summary>Advances one step of the factoring polynomial <c>y² + addend</c> modulo <paramref name="modulus"/>.</summary>
    /// <param name="addend">The polynomial offset distinguishing one cycle walk from another.</param>
    /// <param name="inverse">The negated inverse of <paramref name="modulus"/> obtained from <see cref="GetMontgomeryInverse(uint)"/>.</param>
    /// <param name="modulus">The odd modulus.</param>
    /// <param name="value">The residue to advance; must be less than <paramref name="modulus"/>.</param>
    /// <returns>The advanced residue, less than <paramref name="modulus"/>.</returns>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static uint AdvancePolynomial(uint addend, uint inverse, uint modulus, uint value) {
        var advanced = (((ulong)MontgomeryMultiply(
            left: value,
            modulus: modulus,
            modulusInverse: inverse,
            right: value
        )) + addend);

        return ((uint)((advanced < modulus)
            ? advanced
            : (advanced - modulus)));
    }
    /// <summary>Computes the greatest common divisor of <paramref name="left"/> and <paramref name="right"/> by binary reduction.</summary>
    /// <param name="left">The first operand.</param>
    /// <param name="right">The second operand; must be odd.</param>
    /// <returns>The greatest common divisor of the operands.</returns>
    private static uint GreatestCommonDivisor(uint left, uint right) {
        if (0U == left) { return right; }

        left >>= ((int)uint.TrailingZeroCount(value: left));

        while (left != right) {
            if (left < right) {
                (left, right) = (right, left);
            }

            left -= right;
            left >>= ((int)uint.TrailingZeroCount(value: left));
        }

        return left;
    }
    /// <summary>Runs one Brent cycle walk over the polynomial <c>y² + addend</c> modulo <paramref name="modulus"/>.</summary>
    /// <param name="addend">The polynomial offset for this walk.</param>
    /// <param name="inverse">The negated inverse of <paramref name="modulus"/> obtained from <see cref="GetMontgomeryInverse(uint)"/>.</param>
    /// <param name="modulus">The odd composite to split.</param>
    /// <returns>A divisor of <paramref name="modulus"/>; the walk failed when the result is <c>1</c> or <paramref name="modulus"/> itself.</returns>
    private static uint FindFactorCycle(uint addend, uint inverse, uint modulus) {
        var divisor = 1U;
        var product = 1U;
        var range = 1U;
        var anchor = 0U;
        var backtrack = 0U;
        var y = 2U;

        do {
            anchor = y;

            for (var i = 0U; (i < range); ++i) {
                y = AdvancePolynomial(
                    addend: addend,
                    inverse: inverse,
                    modulus: modulus,
                    value: y
                );
            }

            var k = 0U;

            do {
                backtrack = y;

                var limit = Math.Min(
                    val1: 32U,
                    val2: (range - k)
                );

                for (var i = 0U; (i < limit); ++i) {
                    y = AdvancePolynomial(
                        addend: addend,
                        inverse: inverse,
                        modulus: modulus,
                        value: y
                    );
                    product = MontgomeryMultiply(
                        left: product,
                        modulus: modulus,
                        modulusInverse: inverse,
                        right: ((anchor > y)
                        ? (anchor - y)
                        : (y - anchor))
                    );
                }

                divisor = GreatestCommonDivisor(
                    left: product,
                    right: modulus
                );
                k += 32U;
            } while ((k < range) && (1U == divisor));

            range <<= 1;
        } while (1U == divisor);

        if (divisor == modulus) {
            do {
                backtrack = AdvancePolynomial(
                    addend: addend,
                    inverse: inverse,
                    modulus: modulus,
                    value: backtrack
                );
                divisor = GreatestCommonDivisor(
                    left: ((anchor > backtrack)
                    ? (anchor - backtrack)
                    : (backtrack - anchor)),
                    right: modulus
                );
            } while (1U == divisor);
        }

        return divisor;
    }
    /// <summary>Returns a nontrivial divisor of the odd composite <paramref name="value"/>, whose prime factors must all exceed fifty-nine.</summary>
    /// <param name="value">The composite to split.</param>
    /// <returns>A divisor strictly between <c>1</c> and <paramref name="value"/>; it is not necessarily prime.</returns>
    /// <remarks>The polynomial offset advances through a fixed sequence until a walk succeeds, so the split is deterministic.</remarks>
    private static uint FindFactor(uint value) {
        var inverse = GetMontgomeryInverse(modulus: value);
        var addend = 1U;

        while (true) {
            var divisor = FindFactorCycle(
                addend: addend,
                inverse: inverse,
                modulus: value
            );

            if (
                (1U < divisor) &&
                (divisor < value)
            ) { return divisor; }

            ++addend;
        }
    }
    /// <summary>Subtracts <paramref name="subtrahend"/> from every element of <paramref name="values"/> in place.</summary>
    /// <param name="subtrahend">The amount removed from each element.</param>
    /// <param name="values">The elements to adjust.</param>
    private static void SubtractInPlace(uint subtrahend, Span<uint> values) {
        if (
            Vector.IsHardwareAccelerated &&
            (Vector<uint>.Count <= values.Length)
        ) {
            var scalar = new Vector<uint>(value: subtrahend);
            var vectors = MemoryMarshal.Cast<uint, Vector<uint>>(span: values);

            for (var i = 0; (i < vectors.Length); ++i) { vectors[i] -= scalar; }

            values = values[(vectors.Length * Vector<uint>.Count)..];
        }

        for (var i = 0; (i < values.Length); ++i) { values[i] -= subtrahend; }
    }
    /// <summary>Builds the process-wide table of odd primes below 65,536.</summary>
    /// <returns>The 6,541 primes required by every 32-bit window sieve.</returns>
    private static uint[] CreateBasePrimes() {
        var basePrimes = new uint[6541];
        var composite = new bool[65536];
        var count = 0;

        for (var candidate = 3; (candidate < 65536); candidate += 2) {
            if (composite[candidate]) { continue; }

            basePrimes[count++] = ((uint)candidate);

            for (var m = (((long)candidate) * candidate); (m < 65536L); m += (((long)candidate) << 1)) { composite[m] = true; }
        }

        return basePrimes;
    }
    /// <summary>Sieves the odd values <c>low + 2i</c> for <c>i</c> in <c>[0, bits)</c>, setting bit <c>i</c> of <paramref name="bitmap"/> when the value is composite.</summary>
    /// <param name="basePrimes">The ascending odd primes obtained from <see cref="CreateBasePrimes"/>.</param>
    /// <param name="bitmap">The destination bitmap; its tail word is left unmasked.</param>
    /// <param name="bits">The number of odd values in the window.</param>
    /// <param name="low">The first (odd) value of the window; must be at least <c>3</c>.</param>
    private static void MarkWindow(uint[] basePrimes, ulong[] bitmap, ulong bits, ulong low) {
        var high = (low + ((bits - 1UL) << 1));

        Array.Clear(
            array: bitmap,
            index: 0,
            length: ((int)((bits + 63UL) >> 6))
        );

        for (var i = 0; (i < basePrimes.Length); ++i) {
            var prime = ((ulong)basePrimes[i]);
            var start = (prime * prime);

            if (start > high) { break; }
            if (start < low) {
                var quotient = ((low + (prime - 1UL)) / prime);

                if (0UL == (quotient & 1UL)) { ++quotient; }

                start = (quotient * prime);
            }

            for (var m = start; (m <= high); m += (prime << 1)) {
                var bit = ((m - low) >> 1);

                bitmap[(bit >> 6)] |= (1UL << ((int)(bit & 63UL)));
            }
        }
    }
    /// <summary>Returns the <paramref name="remaining"/>-th prime strictly greater than <paramref name="start"/> by sieving forward windows.</summary>
    /// <param name="gap">The expected spacing between consecutive primes near <paramref name="start"/>, used to size the windows.</param>
    /// <param name="remaining">The number of primes to advance past; must be positive.</param>
    /// <param name="start">The (odd) value the walk begins above.</param>
    private static uint WalkForward(double gap, uint remaining, uint start) {
        var bitmap = ArrayPool<ulong>.Shared.Rent(minimumLength: 16384);

        try {
            var cursor = (((ulong)start) + 2UL);

            while (true) {
                var span = Math.Min(
                    val1: 2_097_150UL,
                    val2: Math.Max(
                        val1: 4096UL,
                        val2: (((ulong)(remaining * gap)) + 1024UL)
                    )
                ) & ~1UL;
                var high = Math.Min(
                    val1: (cursor + span),
                    val2: 4_294_967_295UL
                );
                var bits = (((high - cursor) >> 1) + 1UL);
                var words = ((int)((bits + 63UL) >> 6));
                var tail = ((((ulong)words) << 6) - bits);

                MarkWindow(
                    basePrimes: WindowSieve.BasePrimes,
                    bitmap: bitmap,
                    bits: bits,
                    low: cursor
                );

                if (0UL != tail) { bitmap[(words - 1)] |= (ulong.MaxValue << ((int)(64UL - tail))); }

                for (var word = 0; (word < words); ++word) {
                    var candidates = (~bitmap[word]);
                    var matches = ((uint)BitOperations.PopCount(value: candidates));

                    if (matches < remaining) {
                        remaining -= matches;

                        continue;
                    }

                    while (1U < remaining) {
                        candidates &= (candidates - 1UL);
                        --remaining;
                    }

                    return ((uint)(cursor + (((((ulong)word) << 6) + ((ulong)BitOperations.TrailingZeroCount(value: candidates))) << 1)));
                }

                cursor = (high + 2UL);
            }
        } finally {
            ArrayPool<ulong>.Shared.Return(array: bitmap);
        }
    }
    /// <summary>Returns the <paramref name="remaining"/>-th prime at or below <paramref name="start"/>, counting downward, by sieving backward windows.</summary>
    /// <param name="gap">The expected spacing between consecutive primes near <paramref name="start"/>, used to size the windows.</param>
    /// <param name="remaining">The number of primes to encounter, counting <paramref name="start"/> itself; must be positive.</param>
    /// <param name="start">The (odd) value the walk begins at.</param>
    private static uint WalkBackward(double gap, uint remaining, uint start) {
        var bitmap = ArrayPool<ulong>.Shared.Rent(minimumLength: 16384);

        try {
            var cursor = ((ulong)start);

            while (true) {
                var span = Math.Min(
                    val1: 2_097_150UL,
                    val2: Math.Max(
                        val1: 4096UL,
                        val2: (((ulong)(remaining * gap)) + 1024UL)
                    )
                ) & ~1UL;
                var low = ((cursor > (span + 3UL))
                    ? (cursor - span)
                    : 3UL);
                var bits = (((cursor - low) >> 1) + 1UL);
                var words = ((int)((bits + 63UL) >> 6));
                var tail = ((((ulong)words) << 6) - bits);

                MarkWindow(
                    basePrimes: WindowSieve.BasePrimes,
                    bitmap: bitmap,
                    bits: bits,
                    low: low
                );

                if (0UL != tail) { bitmap[(words - 1)] |= (ulong.MaxValue << ((int)(64UL - tail))); }

                for (var word = (words - 1); (0 <= word); --word) {
                    var candidates = (~bitmap[word]);
                    var matches = ((uint)BitOperations.PopCount(value: candidates));

                    if (matches < remaining) {
                        remaining -= matches;

                        continue;
                    }

                    while (1U < remaining) {
                        candidates ^= (1UL << (63 - BitOperations.LeadingZeroCount(value: candidates)));
                        --remaining;
                    }

                    return ((uint)(low + (((((ulong)word) << 6) + ((ulong)(63 - BitOperations.LeadingZeroCount(value: candidates)))) << 1)));
                }

                cursor = (low - 2UL);
            }
        } finally {
            ArrayPool<ulong>.Shared.Return(array: bitmap);
        }
    }

    /// <summary>Writes the prime factors of <paramref name="value"/>, with multiplicity and in ascending order, into <paramref name="destination"/>.</summary>
    /// <param name="value">The value to factor.</param>
    /// <param name="destination">The destination for the factors; thirty-two entries always suffice.</param>
    /// <returns>
    /// The number of factors written (for example, <c>360</c> writes <c>2, 2, 2, 3, 3, 5</c>), or <c>0</c> when
    /// <paramref name="value"/> is prime or less than two, since neither case has a proper factorization to report.
    /// </returns>
    /// <remarks>
    /// Factors through fifty-nine are removed by reciprocal multiplication — the inverse-product is the exact quotient
    /// whenever it stays under the paired ceiling — and the remaining cofactor is split recursively by Brent cycle
    /// walks over the Montgomery kernel, with each piece settled by <see cref="IsPrime(uint)"/>. Every step is
    /// deterministic, so the same input always factors identically.
    /// </remarks>
    public static int Factorize(this uint value, Span<uint> destination) {
        if (
            (2U > value) ||
            value.IsPrime()
        ) { return 0; }

        var count = 0;

        while (0U == (value & 1U)) {
            destination[count++] = 2U;
            value >>= 1;
        }

        var primes = SmallFactorPrimes;
        var ceilings = SmallFactorCeilings;
        var inverses = SmallFactorInverses;

        for (var i = 0; (i < primes.Length); ++i) {
            var prime = primes[i];

            if ((((ulong)prime) * prime) > value) { break; }

            var ceiling = ceilings[i];
            var inverse = inverses[i];

            while (true) {
                var quotient = (value * inverse);

                if (quotient > ceiling) { break; }

                destination[count++] = prime;
                value = quotient;
            }
        }

        if (1U == value) { return count; }

        var sorted = count;
        var depth = 0;
        Span<uint> pending = stackalloc uint[8];

        pending[depth++] = value;

        while (0 < depth) {
            var cofactor = pending[--depth];

            if (cofactor.IsPrime()) {
                destination[count++] = cofactor;

                continue;
            }

            var divisor = FindFactor(value: cofactor);

            pending[depth++] = divisor;
            pending[depth++] = (cofactor / divisor);
        }

        for (var i = (sorted + 1); (i < count); ++i) {
            var current = destination[i];
            var j = (i - 1);

            while (
                (j >= sorted) &&
                (destination[j] > current)
            ) {
                destination[(j + 1)] = destination[j];
                --j;
            }

            destination[(j + 1)] = current;
        }

        return count;
    }
    /// <summary>Determines whether <paramref name="value"/> is a prime number.</summary>
    /// <param name="value">The value to test.</param>
    /// <returns><see langword="true"/> when <paramref name="value"/> is prime; otherwise <see langword="false"/>.</returns>
    /// <remarks>
    /// Small factors are settled by reciprocal-multiplication trial division against the odd primes through fifty-
    /// nine — two vector comparisons when 256-bit hardware is available, a scalar ladder through thirty-seven
    /// otherwise — after which every survivor below the square of the next untested prime is itself prime. Larger
    /// values are decided by a single base-2 strong-probable-prime round corrected by
    /// <see cref="StrongPseudoprimesBase2"/>, the complete list of composites that fool the round, making the answer
    /// exact over the whole 32-bit range. The round runs in Montgomery form, so the inner loop performs no hardware
    /// division, and the correction consults <see cref="StrongPseudoprimeFilter"/> first, so the binary search almost
    /// never runs.
    /// </remarks>
    public static bool IsPrime(this uint value) {
        if (0U == (value & 01U)) { return (02U == value); }
        if (Vector256.IsHardwareAccelerated) {
            var broadcast = Vector256.Create(value: value);
            var divisible = Vector256.LessThanOrEqual(
                left: (broadcast * LadderDivisorInversesLow),
                right: LadderQuotientCeilingsLow
            ) |
                Vector256.LessThanOrEqual(
                left: (broadcast * LadderDivisorInversesHigh),
                right: LadderQuotientCeilingsHigh
            );

            if (Vector256<uint>.Zero != divisible) {
                return (value is 3U or 5U or 7U or 11U or 13U or 17U or 19U or 23U or 29U or 31U or 37U or 41U or 43U or 47U or 53U or 59U);
            }
            if (3721U > value) { return (1U < value); }
        } else {
            if (0U == (value % 03U)) { return (03U == value); }
            if (0U == (value % 05U)) { return (05U == value); }
            if (0U == (value % 07U)) { return (07U == value); }
            if (0U == (value % 11U)) { return (11U == value); }
            if (0U == (value % 13U)) { return (13U == value); }
            if (0U == (value % 17U)) { return (17U == value); }
            if (0U == (value % 19U)) { return (19U == value); }
            if (0U == (value % 23U)) { return (23U == value); }
            if (0U == (value % 29U)) { return (29U == value); }
            if (0U == (value % 31U)) { return (31U == value); }
            if (0U == (value % 37U)) { return (37U == value); }
            if (1681U > value) { return (1U < value); }
        }

        var exponent = (value - 1U);
        var shift = ((int)uint.TrailingZeroCount(value: exponent));
        var index = (exponent >> shift);
        var inverse = GetMontgomeryInverse(modulus: value);
        var one = ((uint.MaxValue % value) + 1U);
        var minusOne = (value - one);
        var witness = MontgomeryDouble(
            modulus: value,
            residue: one
        );
        var result = one;

        while (true) {
            if (0U != (index & 1U)) {
                result = MontgomeryMultiply(
                    left: result,
                    modulus: value,
                    modulusInverse: inverse,
                    right: witness
                );
            }

            index >>= 1;

            if (0U == index) { break; }

            witness = MontgomeryMultiply(
                left: witness,
                modulus: value,
                modulusInverse: inverse,
                right: witness
            );
        }

        if (
            (one != result) &&
            (minusOne != result)
        ) {
            do {
                if (0 == --shift) { return false; }

                result = MontgomeryMultiply(
                    left: result,
                    modulus: value,
                    modulusInverse: inverse,
                    right: result
                );
            } while (minusOne != result);
        }

        var fingerprint = ((value * 0x9E3779B1U) >> 16);

        if (0UL == (StrongPseudoprimeFilter[(fingerprint >> 6)] & (1UL << ((int)(fingerprint & 63U))))) { return true; }

        return (0 > StrongPseudoprimesBase2.BinarySearch(comparable: value));
    }
    /// <summary>Returns the prime at the zero-based index <paramref name="value"/>.</summary>
    /// <param name="value">The zero-based prime index, where <c>0</c> maps to <c>2</c>, <c>1</c> to <c>3</c>, <c>2</c> to <c>5</c>, and so on.</param>
    /// <returns>
    /// The prime at index <paramref name="value"/>, or <c>0</c> when <paramref name="value"/> exceeds <c>203280220</c> —
    /// the largest index addressable within the 32-bit range, since there are 203,280,221 primes below 2³².
    /// </returns>
    /// <remarks>
    /// Cipolla's asymptotic expansion of the n-th prime seeds the search; the seed is aligned exactly with
    /// <see cref="PrimeCountingFunction(uint)"/> and the residual error is walked off in whichever direction the
    /// target lies — with <see cref="IsPrime(uint)"/> when the target is a handful of primes away, and with a
    /// windowed sieve otherwise — so the answer does not depend on the seed erring to one side.
    /// </remarks>
    public static uint NthPrime(this uint value) {
        if (203280220U < value) { return 0U; }
        if (3U > value) {
            return ((0U == value)
                ? 2U
                : ((1U == value)
                    ? 3U
                    : 5U));
        }

        var count = (value + 1U);
        var logarithm = Math.Log(d: count);
        var logLogarithm = Math.Log(d: logarithm);
        var estimate = (count * ((((logarithm + logLogarithm) - 1.0d) + ((logLogarithm - 2.0d) / logarithm)) - ((((logLogarithm * logLogarithm) - (6.0d * logLogarithm)) + 11.0d) / ((2.0d * logarithm) * logarithm))));
        var factor = ((estimate < 11.0d)
            ? 11U
            : ((4294967291.0d < estimate)
                ? 4294967291U
                : ((uint)estimate))) | 1U;
        var gap = ((logarithm + logLogarithm) + 2.0d);
        var index = factor.PrimeCountingFunction();

        if (index < count) {
            var remaining = (count - index);

            if (16U >= remaining) {
                do {
                    factor += 2U;

                    if (factor.IsPrime()) { ++index; }
                } while (index < count);

                return factor;
            }

            return WalkForward(
                gap: gap,
                remaining: remaining,
                start: factor
            );
        }

        var backward = ((index - count) + 1U);

        if (16U >= backward) {
            while (true) {
                if (factor.IsPrime()) {
                    if (index == count) { return factor; }

                    --index;
                }

                factor -= 2U;
            }
        }

        return WalkBackward(
            gap: gap,
            remaining: backward,
            start: factor
        );
    }
    /// <summary>Returns the number of primes less than or equal to <paramref name="value"/> (the prime-counting function π).</summary>
    /// <param name="value">The inclusive upper bound on the primes to count.</param>
    /// <returns>The count of primes not exceeding <paramref name="value"/>.</returns>
    /// <remarks>
    /// Implements a sublinear combinatorial prime-counting method: a Meissel-style sieve that tracks the running
    /// count-of-primes for both small values directly and large values (<c>value / k</c>) via a shared array, split at
    /// the square root of <paramref name="value"/> and updated one prime at a time. The bulk range updates are
    /// vectorized and the summation-phase divisions reduce through precomputed reciprocals. Working storage is rented
    /// from <see cref="ArrayPool{T}"/> and bounded by the square root of <paramref name="value"/>, so peak usage does
    /// not exceed roughly 768&#160;kilobytes.
    /// </remarks>
    public static uint PrimeCountingFunction(this uint value) {
        if (value < 9U) {
            return ((value < 2U)
                ? 0U
                : ((value < 3U)
                    ? 1U
                    : ((value < 5U)
                        ? 2U
                        : ((value < 7U)
                            ? 3U
                            : 4U))));
        }

        var squareRoot = ((uint)Math.Sqrt(d: value));
        var squareRootHalved = ((squareRoot + 1U) >> 1);
        var ignore = ArrayPool<bool>.Shared.Rent(minimumLength: ((int)(squareRoot + 1U)));
        var larges = ArrayPool<uint>.Shared.Rent(minimumLength: ((int)squareRootHalved));
        var multipliers = ArrayPool<ulong>.Shared.Rent(minimumLength: ((int)squareRootHalved));
        var roughs = ArrayPool<uint>.Shared.Rent(minimumLength: ((int)squareRootHalved));
        var smalls = ArrayPool<uint>.Shared.Rent(minimumLength: ((int)squareRootHalved));

        try {
            for (var index = 0U; (index < squareRootHalved); ++index) {
                larges[index] = (((value / ((index << 1) + 1U)) - 1U) >> 1);
                roughs[index] = ((index << 1) + 1U);
                smalls[index] = index;
            }

            var counter = 0U;
            var factor = 3U;

            if (factor <= squareRoot) {
                Array.Clear(
                    array: ignore,
                    index: 0,
                    length: ((int)(squareRoot + 1U))
                );

                do {
                    if (!ignore[factor]) {
                        var factorSquared = (factor * factor);

                        if ((((ulong)factorSquared) * factorSquared) > value) { break; }

                        ignore[factor] = true;

                        var i = factorSquared;
                        var j = 0U;
                        var k = 0U;

                        while (i <= squareRoot) {
                            ignore[i] = true;
                            i += (factor << 1);
                        }
                        while (j < squareRootHalved) {
                            i = roughs[j];

                            if (!ignore[i]) {
                                var x = (i * factor);
                                var y = ((x > squareRoot)
                                    ? smalls[(((value / x) - 1U) >> 1)]
                                    : larges[(smalls[(x >> 1)] - counter)]);
                                var z = (larges[j] - y);

                                larges[k] = (z + counter);
                                roughs[k++] = i;
                            }

                            ++j;
                        }

                        i = ((squareRoot - 1U) >> 1);
                        j = ((squareRoot / factor) - 1U) | 1U;
                        squareRootHalved = k;

                        while (j >= factor) {
                            var x = (smalls[(j >> 1)] - counter);

                            k = ((j * factor) >> 1);

                            if (k <= i) {
                                SubtractInPlace(
                                    subtrahend: x,
                                    values: smalls.AsSpan(
                                        start: ((int)k),
                                        length: ((int)((i - k) + 1U))
                                    )
                                );

                                i = (k - 1U);
                            }

                            j -= 2U;
                        }

                        ++counter;
                    }

                    factor += 2U;
                } while (factor <= squareRoot);
            }

            larges[0] += (((squareRootHalved + ((counter - 1U) << 1)) * (squareRootHalved - 1U)) >> 1);

            for (var i = 1U; (i < squareRootHalved); ++i) { larges[0] -= larges[i]; }
            for (var i = 1U; (i < squareRootHalved); ++i) { multipliers[i] = ((ulong.MaxValue / roughs[i]) + 1UL); }
            for (var i = 1U; (i < squareRootHalved); ++i) {
                var x = ((uint)((((UInt128)multipliers[i]) * value) >> 64));
                var y = (smalls[((((uint)((((UInt128)multipliers[i]) * x) >> 64)) - 1U) >> 1)] - counter);

                if (y < (i + 1U)) { break; }

                var z = 0U;

                for (var j = (i + 1U); (j <= y); ++j) {
                    z += smalls[((((uint)((((UInt128)multipliers[j]) * x) >> 64)) - 1U) >> 1)];
                }

                larges[0] += (z - ((y - i) * ((counter + i) - 1U)));
            }

            return (larges[0] + 1U);
        } finally {
            ArrayPool<uint>.Shared.Return(array: smalls);
            ArrayPool<uint>.Shared.Return(array: roughs);
            ArrayPool<ulong>.Shared.Return(array: multipliers);
            ArrayPool<uint>.Shared.Return(array: larges);
            ArrayPool<bool>.Shared.Return(array: ignore);
        }
    }
